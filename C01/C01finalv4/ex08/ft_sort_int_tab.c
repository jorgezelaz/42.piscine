void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	aux;
	int	swap;

	swap = 1;
	while (swap == 1)
	{
		swap = 0;
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				aux = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = aux;
				swap = 1;
			}
			i++;
		}
	}
}
