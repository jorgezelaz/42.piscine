void	ft_ultimate_div_mod(int *a, int *b)
{
	int	num1;
	int	num2;
	int	resultado;
	int	resto;

	num1 / num2 = resultado;
	num1 % num2 = resto;
	*a = &resultado;
	*b = &resto;
}
